// Random numbers in js

let x = Math.random();   //random value from 0 to 1 
document.write(x,"<br>");

let y = Math.random()*10;  // random value from 0 to 9
document.write(y,"<br>");

let z = Math.floor(Math.random()*10); // random value from 0 to 9 without decimal
document.write(z,"<br>")

let w = Math.floor(Math.random()*11);  // 0 to 10 
document.write(w);